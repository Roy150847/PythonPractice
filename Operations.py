str1 = input("Enter the first string(str1):")
str2 = input("Enter the second string(str2):")
n1 = int(input("Enter the first integer(n1):"))

# HEXADECIMAL VALUE
hex1 = hex(n1)
print("Hexadecimal value of n1 =",hex1)

# UNICODE NUMBERS
unicode1 = ord(str1)
unicode2 = ord(str2)
print("Unicode of str1 =",unicode1)
print("Unicode of str2 =",unicode2)
char1 = chr(n1)
print("Character of unicode n1 =",char1)