first = int(input(" Enter first number :"))
operator = input(" Enter the operator(+, -, *, /, %) :")
second = int(input(" enter the scond number :"))
if operator == "+" :
    print(first + second)
if operator == "-" :
    print(first - second)
if operator == "*" :
    print(first * second)
if operator == "/" :
    print(first / second)
if operator == "%" :
    print(first % second)
